);

/*
--产品表
--(产品ID/产品名称/供应商ID/产品类别ID/单位数量/单价/库存)
*/
CREATE TABLE  DEMO_REPORTPRODUCTS (
	$clientDefine,
	RPRODUCTID VARCHAR(100) NOT NULL ,
	RPRODUCTNAME VARCHAR(100),
