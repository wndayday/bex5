	RREGION VARCHAR(100),
	RPOSTALCODE VARCHAR(100),
	RCOUNTRY VARCHAR(100),
	RPHONE VARCHAR(100),
	RFAX VARCHAR(100)
);

/*
--订单表
--(订单ID/顾客ID/雇员ID/订单日期/所需日期/发货日期/中转次数/运费/货运名称/货运地址/货运城市/货运地区/货运邮编/货运国家)
*/
