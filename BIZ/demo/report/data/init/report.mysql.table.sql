--(单位ID/单位名称/单位描述)

CREATE TABLE  DEMO_REPORTORG (
	$clientDefine,
	RORGID VARCHAR(100) NOT NULL ,
	RORGNAME VARCHAR(100),
	RORGDESC TEXT
); 
/*
--部门表
--(部门ID/单位ID/部门名称/部门描述)
*/
